<template>
  <v-app>
    <v-navigation-drawer
      v-model="drawer"
      :mini-variant="miniVariant"
      :clipped="clipped"
      fixed
      app
    >
      <v-list>
        <v-list-item
          v-for="(item, i) in toItems"
          :key="i"
          :to="item.to"
          router
          exact
        >
          <v-list-item-action>
            <v-icon>{{ item.icon }}</v-icon>
          </v-list-item-action>
          <v-list-item-content>
            <v-list-item-title v-text="item.title" />
          </v-list-item-content>
        </v-list-item>
      </v-list>
      <v-list>
        <v-list-item
          v-for="(item, i) in clickItems"
          :key="i"
          @click="item.click"
          router
          exact
        >
          <v-list-item-action>
            <v-icon>{{ item.icon }}</v-icon>
          </v-list-item-action>
          <v-list-item-content>
            <v-list-item-title v-text="item.title" />
          </v-list-item-content>
        </v-list-item>
      </v-list>
    </v-navigation-drawer>
    <v-app-bar :clipped-left="clipped" fixed app>
      <v-app-bar-nav-icon @click.stop="drawer = !drawer" />
      <v-btn icon @click.stop="miniVariant = !miniVariant">
        <v-icon>mdi-{{ `chevron-${miniVariant ? 'right' : 'left'}` }}</v-icon>
      </v-btn>
      <v-btn icon @click.stop="clipped = !clipped">
        <v-icon>mdi-application</v-icon>
      </v-btn>
      <v-btn icon @click.stop="fixed = !fixed">
        <v-icon>mdi-minus</v-icon>
      </v-btn>
      <v-toolbar-title v-text="title" />
    </v-app-bar>
    <v-content>
      <v-container>
        <nuxt />
      </v-container>
    </v-content>
    <v-footer :fixed="fixed" app>
      <span>&copy; 2019</span>
    </v-footer>
  </v-app>
</template>

<script lang="ts">
import { Vue, Component, Emit } from 'vue-property-decorator'
import { WebAuthnPage } from '../store'

export interface ToItem {
  icon: string
  title: string
  to: string
}

export interface ClickItem {
  icon: string
  title: string
  click: Function
}

@Component
export default class DefaultLayout extends Vue {
  clipped: Boolean = false
  drawer: Boolean = false
  fixed: Boolean = false
  toItems: Array<ToItem> = [
    {
      icon: 'mdi-apps',
      title: 'Welcome',
      to: '/'
    }
  ]

  clickItems: Array<ClickItem> = [
    {
      icon: 'mdi-apps',
      title: 'Sogn In',
      click: this.showSignIn
    },
    {
      icon: 'mdi-apps',
      title: 'Sogn Up',
      click: this.showSignUp
    }
  ]

  miniVariant: Boolean = false
  right: Boolean = true
  rightDrawer: Boolean = false
  title: string = 'WebAuthn Example'

  @Emit()
  showSignIn() {
    this.$accessor.changePage(WebAuthnPage.SignIn)
  }

  @Emit()
  showSignUp() {
    this.$accessor.changePage(WebAuthnPage.SignUp)
  }
}
</script>

